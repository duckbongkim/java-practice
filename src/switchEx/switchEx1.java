package switchEx;

public class switchEx1 {
    public static void main(String[] args) {
        String score = "B";

        switch(score){
            case "A":
                System.out.println("탁월함");
                break;
            case "B":
                System.out.println("좋음");
                break;
            case "C":
                System.out.println("준수");
                break;
            case "D":
                System.out.println("향상");

        }
    }
}
